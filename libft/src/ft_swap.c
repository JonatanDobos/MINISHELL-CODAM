#include "../libft.h"

void
	ft_swap(void *a, void *b, size_t size)
{
	unsigned char	*temp;

	temp = (unsigned char *)malloc(size);
	if (temp == NULL)
		return ;
	ft_memcpy(temp, a, size);
	ft_memcpy(a, b, size);
	ft_memcpy(b, temp, size);
	free(temp);
}
